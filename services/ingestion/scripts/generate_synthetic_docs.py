def _get_clients():
    """Instantiate Azure OpenAI and Azure AI Search clients from environment."""
    openai_endpoint = os.environ["AZURE_OPENAI_ENDPOINT"]
    openai_api_key = os.environ["AZURE_OPENAI_API_KEY"]
    search_endpoint = os.environ["AZURE_SEARCH_ENDPOINT"]
    search_api_key = os.environ["AZURE_SEARCH_API_KEY"]
    index_name = os.environ["AZURE_SEARCH_INDEX_NAME"]

    oai_client = AzureOpenAI(
        azure_endpoint=openai_endpoint,
        api_key=openai_api_key,
        api_version="2024-02-01",
    )

    search_client = SearchClient(
        endpoint=search_endpoint,
        index_name=index_name,
        credential=AzureKeyCredential(search_api_key),
    )

    return oai_client, search_client


def _count_existing_synthetic_docs(search_client: SearchClient) -> int:
    """Return count of documents in the index with source='synthetic'."""
    results = search_client.search(
        search_text="*",
        filter="source eq 'synthetic'",
        select=["id"],
        include_total_count=True,
        top=0,
    )
    count = results.get_count()
    return count if count is not None else 0


def _generate_document(oai_client: AzureOpenAI, spec: dict) -> str:
    """Call GPT-4o with a structured prompt and return the generated document text."""
    system_message = (
        "You are a senior infrastructure engineering consultant at a global firm. "
        "Write professional, domain-realistic technical documents. Reference real standards, "
        "programs, and regulatory frameworks accurately. Documents should be suitable for "
        "internal firm knowledge base use by consultants and solutions architects. "
        "Do not add any preamble or meta-commentary — write the document directly."
    )

    user_message = (
        f"Document title: {spec['title']}\n"
        f"Document type: {spec['document_type']}\n"
        f"Domain: {spec['domain']}\n\n"
        f"Instructions: {spec['prompt']}"
    )

    response = oai_client.chat.completions.create(
        model=COMPLETION_MODEL,
        messages=[
            {"role": "system", "content": system_message},
            {"role": "user", "content": user_message},
        ],
        temperature=0.7,
        max_tokens=3000,
    )

    return response.choices[0].message.content.strip()


def _chunk_text(text: str, encoding: tiktoken.Encoding) -> list[str]:
    """Chunk text at CHUNK_SIZE_TOKENS with CHUNK_OVERLAP_TOKENS overlap."""
    tokens = encoding.encode(text)
    chunks = []
    start = 0
    while start < len(tokens):
        end = min(start + CHUNK_SIZE_TOKENS, len(tokens))
        chunks.append(encoding.decode(tokens[start:end]))
        if end == len(tokens):
            break
        start += CHUNK_SIZE_TOKENS - CHUNK_OVERLAP_TOKENS
    return chunks


def _embed(oai_client: AzureOpenAI, text: str) -> list[float]:
    """Embed text using Azure OpenAI text-embedding-ada-002."""
    response = oai_client.embeddings.create(
        model=EMBEDDING_MODEL,
        input=text,
    )
    return response.data[0].embedding


def _slugify(text: str) -> str:
    """Convert text to a URL-safe slug."""
    return re.sub(r"[^a-zA-Z0-9_-]", "_", text.lower())[:80]


def _upsert_chunks(
    search_client: SearchClient,
    oai_client: AzureOpenAI,
    spec: dict,
    doc_text: str,
    encoding: tiktoken.Encoding,
    now_iso: str,
) -> int:
    """Chunk, embed, and upsert a document's chunks into Azure AI Search."""
    chunks = _chunk_text(doc_text, encoding)
    doc_slug = spec["slug"]
    docs_to_upsert = []

    for chunk_idx, chunk_text in enumerate(chunks):
        chunk_id = f"synthetic_{doc_slug}_{chunk_idx}"
        vector = _embed(oai_client, chunk_text)

        docs_to_upsert.append({
            "id": chunk_id,
            "content": chunk_text,
            "content_vector": vector,
            "source": SYNTHETIC_SOURCE,
            "document_type": spec["document_type"],
            "domain": spec["domain"],
            "last_updated": now_iso,
            "chunk_index": chunk_idx,
            "source_url": f"synthetic://{doc_slug}",
        })

    if docs_to_upsert:
        search_client.upsert_documents(documents=docs_to_upsert)

    return len(chunks)


