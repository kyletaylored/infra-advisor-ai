def main():
    """
    Generate and index all 80 synthetic documents.

    Idempotent: checks Azure AI Search for existing synthetic docs first.
    If count >= MIN_SYNTHETIC_DOCS (80), exits immediately.
    """
    log.info("Starting synthetic document generation — checking existing index count...")

    oai_client, search_client = _get_clients()

    existing_count = _count_existing_synthetic_docs(search_client)
    log.info("Found %d existing synthetic documents in Azure AI Search.", existing_count)

    if existing_count >= MIN_SYNTHETIC_DOCS:
        log.info(
            "Index already has %d synthetic documents (>= %d required). "
            "Skipping generation.",
            existing_count,
            MIN_SYNTHETIC_DOCS,
        )
        return

    log.info(
        "Generating %d synthetic documents (%d already indexed)...",
        MIN_SYNTHETIC_DOCS - existing_count,
        existing_count,
    )

    enc = tiktoken.get_encoding("cl100k_base")
    now_iso = datetime.now(timezone.utc).isoformat()

    total_chunks = 0
    generated_count = 0

    for i, spec in enumerate(DOCUMENT_SPECS):
        slug = spec["slug"]
        log.info("[%d/%d] Generating: %s", i + 1, len(DOCUMENT_SPECS), spec["title"])

        try:
            doc_text = _generate_document(oai_client, spec)
            word_count = len(doc_text.split())
            log.info("  Generated %d words for '%s'", word_count, slug)

            chunk_count = _upsert_chunks(
                search_client, oai_client, spec, doc_text, enc, now_iso
            )
            log.info("  Indexed %d chunks for '%s'", chunk_count, slug)
            total_chunks += chunk_count
            generated_count += 1

        except Exception as exc:
            log.error("  ERROR generating '%s': %s", slug, exc, exc_info=True)
            # Continue with remaining documents — do not abort the full run
            continue

    log.info(
        "Synthetic document generation complete. "
        "Generated: %d/%d documents. Total chunks indexed: %d.",
        generated_count,
        len(DOCUMENT_SPECS),
        total_chunks,
    )


if __name__ == "__main__":
    main()
